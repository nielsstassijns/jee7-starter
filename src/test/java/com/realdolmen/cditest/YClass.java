package com.realdolmen.cditest;

public class YClass {
	
	private int numberY;
	private String someTextY;
	
	
	
	public YClass() {
		super();
	}



	public YClass(int numberY, String someTextY) {
		super();
		this.numberY = numberY;
		this.someTextY = someTextY;
	}



	public int getNumberY() {
		return numberY;
	}



	public void setNumberY(int numberY) {
		this.numberY = numberY;
	}



	public String getSomeTextY() {
		return someTextY;
	}



	public void setSomeTextY(String someTextY) {
		this.someTextY = someTextY;
	}
	
	

}
